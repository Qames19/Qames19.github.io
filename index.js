var imageElement = document.querySelector("img");
var submitButton = document.getElementById("weather-submit");

var dateInfo = document.getElementById("date-info");
var timeInfo = document.getElementById("time-info");
var conditionsInfo = document.getElementById("conditions-info");
var temperatureInfo = document.getElementById("temperature-info");
var ambientTempInfo = document.getElementById("ambient-temp-info");
var windInfo = document.getElementById("wind-info");
var humidityInfo = document.getElementById("humidity-info");
var precipitationInfo = document.getElementById("precipitation-info");

submitButton.onclick = fillData;

function fillData(){
    // console.log("button clicked");
    let value = document.getElementById("weather-zip").value;
    // console.log(weather[value]);
    imageElement.src = weather[value].current.condition.icon;
    let dateValue = new Date(weather[value].localtime);
    dateInfo.innerText = dateValue.getMonth() + "-" + dateValue.getDate() + "-" + dateValue.getFullYear();
    timeInfo.innerText = dateValue.getHours() + ":" + dateValue.getMinutes();
    conditionsInfo.innerText = weather[value].current.condition.text;
    temperatureInfo.innerText = weather[value].current.temp_f + "F";
    ambientTempInfo.innerText = weather[value].current.feelslike_f + "F";
    windInfo.innerText = weather[value].current.wind_mph + "mph to the " + weather[value].current.wind_dir;
    humidityInfo.innerText = weather[value].current.humidity + "%";
    precipitationInfo.innerText = weather[value].current.precip_in +"\"";
}

